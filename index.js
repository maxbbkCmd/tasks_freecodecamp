// ‚úÖ
function getAverage(scores) {
  let sum = 0;
  let sumItems = 0;
  let result = 0;

  for (let i = 0; i < scores.length; i++) {
    sum = sum + scores[i];
  }

  for (let i = 0; i < scores.length; i++) {
    sumItems++;
  }
    return result = sum / sumItems;
}

