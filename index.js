// ‚úÖ
function getGrade(score) {
  if (score >= 0 && score <= 59) {
    return "F";
  } else if (score >= 50 && score <= 69) {
    return "D";
  } else if (score >= 70 && score <= 79) {
    return "C";
  } else if (score >= 80 && score <= 89) {
    return "B";
  } else if (score >= 90 && score <= 99) {
    return "A";
  } else if (score == 100) {
    return "A++";
  } else return "Inner number 0-100";
}

